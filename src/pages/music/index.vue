  let songLen = musicList.value.length;
  let index = getIndex();

  if (index + 1 === songLen) {
    // 最后一首
    player(musicList.value[ songLen - 2 ], index)    
  } else if (index === 0) {
    // 第一首
    player(musicList.value[ songLen - 1 ], index)    
  } else if (index > 0 && index + 1 < songLen) {
    index--;
    player(musicList.value[ index ], index)    
  }
