  const p = musicList.value.filter(
    (m: { id: any }) => m.id === currMusic.value.currSong.id
  );
  let index = 0;
  for (let i = 0; i < musicList.value.length; i++) {
    const e = musicList.value[i];
    if (e.id === p[0].id) {
      index = i;
    }
  }
  return index;
};

// 监听isPlay值的变化
watch(isPlay, () => {
  isPlay.value ? audio.value.play() : audio.value.pause();
});

