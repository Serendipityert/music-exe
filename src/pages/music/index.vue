//暂停或播放
const handlePauseOrPlay = () => {
  !isPlay.value ? audio.value.play() : audio.value.pause();
  isPlay.value = !isPlay.value;
};
// 视频在可以播放时触发
const getDuration = () => {
  duration.value = timeFormat(audio.value.duration);
  audio.value = audio.value;
  handlePauseOrPlay();
};
//将单位为秒的的时间转换成mm:ss的形式
const timeFormat = (num: number) => {
  let minute: any = parseInt((num / 60).toString());
  let second: any = parseInt((num % 60).toString());

  minute = minute >= 10 ? minute : "0" + minute;
  second = second >= 10 ? second : "0" + second;
  return minute + ":" + second;
};
//点击进度条更新进度
const clickProgress = (e: { offsetX: any }) => {
  console.log("clickProgress");
  //如果不是正在移动 和 没有暂停播放就执行
  if (!isMoveIn.value || !isPlay.value) {
    updateProgress(e.offsetX);
  }
  handlePauseOrPlay();
};
// 更新进度
const updateProgress = (MoveX: number) => {
  //当前移动的位置 = 当前移动的位置 / 当前进度条的可视长度    //this.$refs.progress.clientWidth 注意一定要拿总长度 否则会拿进度条已经走过的长度
  let clickProgress = MoveX / progress.value.clientWidth;
  //设置播放的时间 = 总时长 * 当前点击的长度
  audio.value.currentTime = audio.value.duration * clickProgress;
  //置移动的位置
  currentProgress.value.style.width = MoveX + "px";
  circle.value.style.left = MoveX - circle.value.clientWidth / 2 + "px";
};

//进度条发生变化时触发
const updateTime = () => {
  if (!progress.value) return;
  currentDuration.value = timeFormat(audio.value.currentTime);
  //如果不是正在移动 和 没有暂停播放就执行
  if (!isMoveIn.value || !isPlay.value) {
    // 设置当前时间
    let MoveX =
      progress.value.clientWidth *
      (audio.value.currentTime / audio.value.duration);
    //播放时更新距离
    currentProgress.value.style.width = MoveX + "px";
    circle.value.style.left = MoveX - circle.value.clientWidth / 2 + "px";
  }
};

const handleClick = () => {
  visible.value = true;
};
const handleClose = () => {
  visible.value = false;
};

