watch([currentDuration, duration], ([curr, curr2], [old, old2]) => {
  if (curr === curr2) {
    isPlay.value = false;
    let index = getIndex();
    let len = musicList.value.length;

    if (playMode.value === 1) {
      // 随机
      player(musicList.value[ Math.floor( Math.random() * len ) ],index)    
    } else if (playMode.value === 2) {
      // 顺序
      if (index + 1 === len) {
        // 当前为最后一首
        player(musicList.value[ 0 ], index)  
      } else if (index < len) {
        index++
        player(musicList.value[ index ], index)  
      }
    } else if (playMode.value === 3) {
      // 单曲
      player(musicList.value[ index ], index)    
    }
    audio.value.load();
  }
